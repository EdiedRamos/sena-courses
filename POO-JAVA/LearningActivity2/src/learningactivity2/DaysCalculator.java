        DaysConverter converter = new DaysConverter(daysInt);
        
        hours.setText(String.valueOf(converter.toHours()));
        minutes.setText(String.valueOf(converter.toMinutes()));
        seconds.setText(String.valueOf(converter.toSeconds()));
        
